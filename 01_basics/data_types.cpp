/*
Topic : Data Types in C++ 
Purpose : 
- Understand primitive data types
- Learn size and range 
- Observe different data types in C++
*/

#include <iostream>
using namespace std;

int main(){
    // Primitive Data Types
    int count = 7;
    float price = 99.99f;
    double pi = 3.141592653589793;
    char grade = 'A';
    bool isPassed = true;

    cout << "Int Value : " << count << endl;
    cout << "Float Value : " << price << endl;
    cout << "Double Value : " << pi << endl;
    cout << "Char Value : " << grade << endl;
    cout << "Bool Value : " << isPassed << endl;

    return 0;
}